    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $requestData = $request->all();
        $question = $requestData['question'];

        //save questions 
        $newQuestions = new Question;
        $newQuestions->question = $question;
        $newQuestions->save();

        $answers = $requestData['answers'];
        foreach ($answers as $answer) {
            $newAnswers = new Answer;
            $newAnswers->answer = $answer['answer'];
            $newAnswers->question_id = $newQuestions->id;
            $newAnswers->correct_answer = $answer['correct_answer'];
            $newAnswers->save();
        }

        return redirect('/questions')->with('success', 'Question and answer create succesfully');
    }

